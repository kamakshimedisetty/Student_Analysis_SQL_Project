ORDER BY marks DESC;

CREATE OR REPLACE FUNCTION get_highest_marks   /*Highest Marks*/
RETURN NUMBER
IS
    max_marks NUMBER;
BEGIN
    SELECT MAX(marks)
    INTO max_marks
    FROM students;

    RETURN max_marks;
END;
/
SELECT get_highest_marks FROM dual;

CREATE OR REPLACE FUNCTION count_slow_learners   /*count slow learners*/
RETURN NUMBER
IS
    cnt NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO cnt
    FROM students
    WHERE marks < 50;

    RETURN cnt;
END;
/

CREATE OR REPLACE FUNCTION count_toppers   /*count toppers*/
RETURN NUMBER
IS
    cnt NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO cnt
    FROM students
    WHERE marks > 75;

    RETURN cnt;
END;
/

BEGIN
   DBMS_OUTPUT.PUT_LINE('Highest Marks: ' || get_highest_marks);
   DBMS_OUTPUT.PUT_LINE('Slow Learners Count: ' || count_slow_learners);
   DBMS_OUTPUT.PUT_LINE('Toppers Count: ' || count_toppers);
END;
/

